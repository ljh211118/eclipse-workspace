package com.lx.animal;

public class Dog {
    
    String name;
    int age;
    String number;
    
  
    
    public void setName(String name) {
        this.name = name;
    }
    
    public void setAge(int age) {
        this.age = age;
    }
    
    public void setNumber(String number) {
        this.number = number;
    }
    
    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
    public String getNumber() {
        return number;
    }
}